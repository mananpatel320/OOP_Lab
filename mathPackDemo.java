//All the given below functions of Math class are static
class mathPackDemo{
	public static void main(String[] args){
		System.out.println(Math.abs(-10));
		double x = 100.45;
		double y = Math.sin(x);
		System.out.println(y);
		y = Math.cos(x);
		System.out.println(y);
		y = Math.tan(x);
		System.out.println(y);
		y = Math.log10(x);
		System.out.println(y);
		y = Math.sqrt(x);
		System.out.println(y);
		y = Math.cbrt(x);
		System.out.println(y);
		y = Math.pow(2,5);
		System.out.println(y);
		y = Math.ceil(x);
		System.out.println(y);
		y = Math.floor(x);
		System.out.println(y);
		y = Math.round(10.50000); //11
		System.out.println(y);
		y = Math.round(10.49999); //10
		System.out.println(y);
	}
}
		
		
